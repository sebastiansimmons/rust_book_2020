// Define a new public trait object, Draw, that requires a fn draw
pub trait Draw {
    fn draw(&self);
}

// Define a struct, Screen, that contains a vector of objects that implement the Draw trait
pub struct Screen {
    pub components: Vec<Box<dyn Draw>>,
}

impl Screen {
    pub fn run(&self) {
        for component in self.components.iter() {
            component.draw();
        }
    }
}

pub struct Button {
    pub width: u32,
    pub height: u32,
    pub label: String,
}

impl Draw for Button {
    fn draw(&self) {
        // Draw the button
    }
}

